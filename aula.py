#Tipo de dado bool (Boolean)
# Ao questionar um programa,
# Só existem dois tipos de resposta possíveis:
# True or False
# Existem vários operadores para "questionar"
# Dentro deles o '==' é o operador lógico que,
# questiona se o valor é igual ao outro.

