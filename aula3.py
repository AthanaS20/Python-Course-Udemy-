# String - são textos que ficam dentro de apas simples ou duplas.

# Escape
print('Athana \' Silva') # O comando \' ou \" é usado para mandar o python ignorar o comando e imprimir a aspa.

# r
print(r'Athana \'Silva\'') #Caso você queria mostrar também a barra com a aspa você pode user o comando r antes das aspas.