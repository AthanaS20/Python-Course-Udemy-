# A função print é feita para imprimir na tela o que foi digitado, é uma função para imprimir.
print(21) # Ao pressionar Ctrl c + Ctrl V ele imprime o mesmo comando nas linhas abaixo.
print(21, 20) #Argumentos nnão noemados
print(21, 10, sep=' ') #O comando sep='' é utilizado para separar um argumento do outro, porém já é padrão do py.
# Você pode coloca algo dentro do sep=' - ' para usar como separador.
print(25, 20, end='\n') #O comando end='' refere-se ao final da linha.
#O comando \n é o comando padrão que o windows reconhece para quebrar linha. 